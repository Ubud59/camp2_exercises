// ## A Rectangle of 10 by 10
//
// ```
// **********
// **********
// **********
// **********
// **********
// **********
// **********
// **********
// **********
// **********
// ```
let i = 0;
for (let j = 0; j < 10; j++) {
  while (i < 10) {
    process.stdout.write("*");
    i++;
  }
  process.stdout.write("\n");
  i = 0;
}
